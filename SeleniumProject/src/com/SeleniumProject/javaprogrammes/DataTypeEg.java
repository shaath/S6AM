package com.SeleniumProject.javaprogrammes;

public class DataTypeEg {

	public static void main(String[] args) {
		
		int intSalary;
		intSalary = 15000;
		System.out.println(intSalary);
		
		double dblPrice = 456.33;
		System.out.println(dblPrice);
		
		char chrDesg = 'M';
		System.out.println(chrDesg);
		
		String strCourse = "Selenium";
		System.out.println(strCourse);
		
		boolean blnFlag = false;
		System.out.println(blnFlag);
		
		intSalary = 60000;
		System.out.println(intSalary);
		
	}

}
